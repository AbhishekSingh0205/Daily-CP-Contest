void solve()
{
    e2(n,m);
    int a[n+1][n+1];memset(a,-1,sizeof a);
    int vis[n+1][n+1];memset(vis,0,sizeof vis);
    set<int>st1;
    for(int i=0;i<10000;i++){
        st1.insert(i*i);
    }
    // pr(st1);
    queue<pii>st;
    vector<pii>req;
    for(auto v:st1){
        
        if(st1.find(m-v)!=st1.end()){
            pr(v,m-v);
            int x=sqrtl(v);
            int y=sqrtl(m-v);
            if(x<=n && y<=n && x>=0 && y>=0){
                a[x][y]=1;
                req.pb({x,y});
            }
        }
    }
    // prm(a,n+1,n+1);
    // pr(req);
    a[1][1]=0;
    st.push({1,1});
    while(st.size()){
        pii tp=st.front();
        int x=tp.ff;
        int y=tp.ss;
        st.pop();
        vis[x][y]=1;
        for(auto v:req){
            for(int i=0;i<4;i++){
                int newX=x+(dx[i]*v.ff);
                int newY=y+(dy[i]*v.ss);
                if(newX>=1 && newX<=n && newY>=1 && newY<=n && vis[newX][newY]==0){
                    vis[newX][newY]=1;
                    a[newX][newY]=a[x][y]+1;
                    st.push({newX,newY});
                }
            }
        }
    }
    for(int i=1;i<=n;i++){
        for(int j=1;j<=n;j++){
            cout<<a[i][j]<<" ";
        }
        cout<<endl;
    }


}
